    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed

        try {
            Registry reg = LocateRegistry.getRegistry("localhost",1212);
            FireAlarmInterface fai = (FireAlarmInterface)reg.lookup("FireAlarmServer");
            boolean result = fai.login(txtUsername.getText(), txtPassword.getText());
//            System.out.println("pw: "+txtPassword.getText());
            if(result){
                JOptionPane.showMessageDialog(null, "Login Successfully");
                console obj = new console();
                obj.setVisible(true);
                this.dispose();
                
            }else{
                JOptionPane.showMessageDialog(null, "Login Failed. Error: credentials mismatch");
            }
            
            
        } catch (RemoteException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println(ex.getMessage());
        } catch (NotBoundException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println(ex.getMessage());
        }
        
        
    }//GEN-LAST:event_btnLoginActionPerformed

